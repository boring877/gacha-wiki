// Migration script: Split monolithic JSON files into per-character JS files
// Usage: node scripts/migrate-sab-data.js
// This reads character-stats.json, characters_info.json, and character-skills.json
// and generates individual character data files under src/data/silver-and-blood/characters/
// PLUS a characters-registry.js that adapters can import instead of the big JSONs

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, '..');
const DATA_DIR = join(ROOT, 'src', 'data', 'silver-and-blood');
const CHAR_DIR = join(DATA_DIR, 'characters');
const LEGACY_DIR = join(DATA_DIR, '_legacy');

const statsData = JSON.parse(readFileSync(join(LEGACY_DIR, 'character-stats.json'), 'utf-8'));
const infoData = JSON.parse(readFileSync(join(LEGACY_DIR, 'characters_info.json'), 'utf-8'));
const skillsData = JSON.parse(readFileSync(join(LEGACY_DIR, 'character-skills.json'), 'utf-8'));

const generateSlug = (name) =>
  name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '');

if (!existsSync(CHAR_DIR)) {
  mkdirSync(CHAR_DIR, { recursive: true });
}

const statsBySlug = new Map();
statsData.characters.forEach((c) => statsBySlug.set(generateSlug(c.name), c));

const infoBySlug = new Map();
infoData.characters.forEach((c) => {
  const slug = generateSlug(c.name);
  if (!infoBySlug.has(slug)) infoBySlug.set(slug, []);
  infoBySlug.get(slug).push(c);
});

const skillsById = new Map();
Object.entries(skillsData).forEach(([id, data]) => skillsById.set(id, data));

const allSlugs = new Set([...statsBySlug.keys(), ...infoBySlug.keys()]);

// Also scan characters/ directory for manually-added files
const charDirFiles = existsSync(CHAR_DIR) ? require('fs').readdirSync(CHAR_DIR).filter(f => f.endsWith('.js') && f !== 'index.js') : [];
charDirFiles.forEach(f => {
  const slug = f.replace('.js', '');
  allSlugs.add(slug);
});

const charactersWithImages = new Set([
  'acappella', 'agares', 'agnes', 'albrecht', 'alene', 'alexi', 'ami', 'augustine', 'bella', 'blooming-empousa', 'cain',
  'cecia', 'dalcarlo', 'darcias', 'edina', 'ethereal-joan', 'fanny', 'fleeting-bella', 'friedrich',
  'genevieve', 'gilrain', 'hati', 'incendiary-agares', 'jinxed-selena', 'joan',
  'julius', 'lamia', 'letitia', 'limine', 'livian', 'lorelei', 'lucille', 'mass', 'nicole', 'ottavia', 'pavana',
  'piera', 'quinn', 'regina', 'ressa', 'resurgent-setti', 'selena', 'seraphina', 'seth', 'sirene', 'spectral-gilrain',
  'starry-eyed-aiona', 'stella', 'theophane', 'thibault', 'timeless-aiona',
  'transcendent-ami', 'transcendent-hati', 'transcendent-noah', 'tris', 'valora',
  'van-helsing', 'yggdrasill'
]);

function buildCharacterFile(slug) {
  const stats = statsBySlug.get(slug);
  const infos = infoBySlug.get(slug) || [];
  const numericId = stats?.id || (infos.length > 0 ? infos[0].id : null);
  const skills = numericId ? skillsById.get(numericId) : null;
  const name = stats?.name || (infos.length > 0 ? infos[0].name : slug);
  const rarity = stats?.rarity || (infos.length > 0 ? infos[0].rarity : null);
  const hasImage = charactersWithImages.has(slug);

  const lines = [];
  lines.push('// Character data: ' + name + ' (slug: ' + slug + ')');
  lines.push('// Auto-generated by scripts/migrate-sab-data.js - do not edit manually');
  lines.push('');

  if (stats) {
    lines.push('export const stats = ' + JSON.stringify(stats, null, 2) + ';');
  } else {
    lines.push('export const stats = null;');
  }
  lines.push('');

  lines.push('export const info = ' + JSON.stringify(infos[0] || null, null, 2) + ';');
  lines.push('');

  lines.push('export const allInfo = ' + JSON.stringify(infos, null, 2) + ';');
  lines.push('');

  if (skills) {
    lines.push('export const skills = ' + JSON.stringify(skills, null, 2) + ';');
  } else {
    lines.push('export const skills = null;');
  }
  lines.push('');

  lines.push('export const meta = {');
  lines.push('  slug: ' + JSON.stringify(slug) + ',');
  lines.push('  name: ' + JSON.stringify(name) + ',');
  lines.push('  numericId: ' + JSON.stringify(numericId) + ',');
  lines.push('  rarity: ' + JSON.stringify(rarity) + ',');
  lines.push('  hasImage: ' + hasImage + ',');
  lines.push('  detailUrl: "/guides/silver-and-blood/characters/' + slug + '"');
  lines.push('};');

  return lines.join('\n');
}

let generated = 0;
const slugList = [];

for (const slug of [...allSlugs].sort()) {
  const content = buildCharacterFile(slug);
  writeFileSync(join(CHAR_DIR, slug + '.js'), content, 'utf-8');
  slugList.push(slug);
  generated++;
}

const indexImports = slugList.map(
  (slug) => 'import { stats as ' + slug.replace(/-/g, '_') + '_stats, info as ' + slug.replace(/-/g, '_') + '_info, allInfo as ' + slug.replace(/-/g, '_') + '_allInfo, skills as ' + slug.replace(/-/g, '_') + '_skills, meta as ' + slug.replace(/-/g, '_') + '_meta } from "./' + slug + '.js";'
).join('\n');

const indexStatsArray = 'export const allStats = [' + slugList.map((s) => s.replace(/-/g, '_') + '_stats').join(', ') + '].filter(Boolean);';
const indexInfoArray = 'export const allInfo = [' + slugList.map((s) => s.replace(/-/g, '_') + '_info').join(', ') + '].filter(Boolean);';
const indexAllInfoArray = 'export const allInfoEntries = [' + slugList.map((s) => '...(' + s.replace(/-/g, '_') + '_allInfo || [])').join(', ') + '].filter(Boolean);';
const indexSkillsObject = 'export const allSkills = {' + slugList.map((s) => {
  const v = s.replace(/-/g, '_') + '_skills';
  return '...(' + v + ' ? { [' + v + '.characterId]: ' + v + ' } : {})';
}).join(', ') + '};';
const indexMetaList = 'export const allMeta = [' + slugList.map((s) => s.replace(/-/g, '_') + '_meta').join(', ') + '].filter(Boolean);';

const indexContent =
  '// Character data registry - auto-generated by scripts/migrate-sab-data.js\n' +
  '// Do not edit manually - run the migration script to regenerate\n\n' +
  indexImports + '\n\n' +
  indexStatsArray + '\n' +
  indexInfoArray + '\n' +
  indexAllInfoArray + '\n' +
  indexSkillsObject + '\n' +
  indexMetaList + '\n';

writeFileSync(join(CHAR_DIR, 'index.js'), indexContent, 'utf-8');

console.log('Generated ' + generated + ' character files in ' + CHAR_DIR);
console.log('Index file: ' + join(CHAR_DIR, 'index.js'));
