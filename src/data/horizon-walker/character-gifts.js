// Helper functions
export const getCharacterById = characterId => {
  return CHARACTER_GIFT_PREFERENCES.find(char => char.id === characterId);
};

export const getCharactersByRarity = rarity => {
  return CHARACTER_GIFT_PREFERENCES.filter(char => char.rarity === rarity);
};

export const getAllCharacters = () => {
  return CHARACTER_GIFT_PREFERENCES;
};

export const searchCharacters = searchTerm => {
  const term = searchTerm.toLowerCase();
  return CHARACTER_GIFT_PREFERENCES.filter(char => char.name.toLowerCase().includes(term));
};

export const getCharacterFavoriteGifts = characterId => {
  const character = getCharacterById(characterId);
  return character?.favorites || [];
};

export const getCharacterLikedGifts = characterId => {
  const character = getCharacterById(characterId);
  return character?.likes || [];
};

export const getAllCharacterPreferences = characterId => {
  const character = getCharacterById(characterId);
  if (!character) return { favorites: [], likes: [] };

  return {
    favorites: character.favorites || [],
    likes: character.likes || [],
  };
};

