// Helper function to get character by slug
export const getCharacterBySlug = slug => {
  return BUSTY_BURST_CHARACTER_INFO[slug];
};

// Helper function to get all characters
export const getAllCharacters = () => {
  return Object.values(BUSTY_BURST_CHARACTER_INFO);
};

// Helper function to get all character slugs
export const getAllCharacterSlugs = () => {
  return Object.keys(BUSTY_BURST_CHARACTER_INFO);
};

// Helper function to get characters by element
export const getCharactersByElement = element => {
  return getAllCharacters().filter(char => char.element === element);
};

// Helper function to get characters by role
export const getCharactersByRole = role => {
  return getAllCharacters().filter(char => char.role === role);
};
