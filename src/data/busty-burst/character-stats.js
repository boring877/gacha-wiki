// Helper functions
export const getCharacterStats = id => {
  return BUSTY_BURST_CHARACTER_STATS[id] || null;
};

export const getAllCharacterStats = () => {
  return Object.values(BUSTY_BURST_CHARACTER_STATS);
};

export const getStatsByElement = element => {
  return Object.values(BUSTY_BURST_CHARACTER_STATS).filter(
    char => char.element.toLowerCase() === element.toLowerCase()
  );
};

export const getStatsByRole = role => {
  return Object.values(BUSTY_BURST_CHARACTER_STATS).filter(
    char => char.role.toLowerCase() === role.toLowerCase()
  );
};

