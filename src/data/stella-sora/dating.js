// Helper function to get dating events by character slug
export function getDatingEventsByCharacter(slug) {
  return datingEvents.find((char) => char.slug === slug);
}

// Helper function to get all events for a specific location
export function getDatingEventsByLocation(location) {
  const results = [];
  datingEvents.forEach((char) => {
    char.events.forEach((event) => {
      if (event.clue.toLowerCase().includes(location.toLowerCase())) {
        results.push({
          character: char.character,
          slug: char.slug,
          ...event,
        });
      }
    });
  });
  return results;
}

