// Helper function to get character by slug
export function getCharacterBySlug(slug) {
  return characterInfo.find(char => char.slug === slug);
}

// Helper function to get characters by element
export function getCharactersByElement(element) {
  return characterInfo.filter(char => char.element === element);
}

// Helper function to get characters by position
export function getCharactersByPosition(position) {
  return characterInfo.filter(char => char.position === position);
}

// Helper function to get characters by faction
export function getCharactersByFaction(faction) {
  return characterInfo.filter(char => char.faction === faction);
}

// Helper function to get characters by grade (rarity)
export function getCharactersByGrade(grade) {
  return characterInfo.filter(char => char.grade === grade);
}

// Get all unique factions
export function getAllFactions() {
  return [...new Set(characterInfo.map(char => char.faction))].sort();
}

// Get all unique styles
export function getAllStyles() {
  return [...new Set(characterInfo.map(char => char.style))].sort();
}
