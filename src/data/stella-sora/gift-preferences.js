// Helper function to get character preferences
export function getCharacterPreferences(characterName) {
  return giftPreferences[characterName] || null;
}

// Helper function to check if a character loves a gift
export function doesCharacterLove(characterName, giftName) {
  const prefs = giftPreferences[characterName];
  return prefs ? prefs.loves.includes(giftName) : false;
}

// Helper function to check if a character hates a gift
export function doesCharacterHate(characterName, giftName) {
  const prefs = giftPreferences[characterName];
  return prefs ? prefs.hates.includes(giftName) : false;
}

// Helper function to get all characters who love a specific gift
export function getCharactersWhoLove(giftName) {
  return Object.entries(giftPreferences)
    .filter(([_, prefs]) => prefs.loves.includes(giftName))
    .map(([name]) => name);
}

// Helper function to get all characters who hate a specific gift
export function getCharactersWhoHate(giftName) {
  return Object.entries(giftPreferences)
    .filter(([_, prefs]) => prefs.hates.includes(giftName))
    .map(([name]) => name);
}

// Get all character names
export function getAllCharacterNames() {
  return Object.keys(giftPreferences);
}

export default giftPreferences;
