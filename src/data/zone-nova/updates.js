// Helper function to get tag display text
export function getTagText(type) {
  const tags = {
    maintenance: 'MAINTENANCE',
    event: 'EVENT',
    recruitment: 'LIMITED RECRUITMENT',
    announcement: 'ANNOUNCEMENT',
  };
  return tags[type] || 'UPDATE';
}

